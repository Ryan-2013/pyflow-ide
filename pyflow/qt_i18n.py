def normalize_language(language: object) -> str:
    value = str(language or "zh_TW")
    return value if value in SUPPORTED_LANGUAGES else "zh_TW"


def tr(interface_language: str, text: str, **values: object) -> str:
    template = (
        ENGLISH.get(text, text)
        if normalize_language(interface_language) == "en"
        else text
    )
    return template.format(**values) if values else template
